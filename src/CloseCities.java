
public class CloseCities {
	private String time;
	private String firstCity;
	private String secondCity;

	public CloseCities(String t, String s1, String s2){
		time = t;
		firstCity = s1;
		secondCity = s2;
	}

	public String getTime() {
		return time; 
	}

	public String getFirstCity() {
		return firstCity;
	}

	public String getSecondCity() {
		return secondCity;
	}
}
